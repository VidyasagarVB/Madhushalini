package KRNifomatix;

public class StaticKeyword {

	public static void main(String[] args) {
		System.out.println("Using Static key word");
         six.Saske();
         six l6 = new six();
         l6.goku();

	}
}
class six{
	public static void Saske() {
		System.out.println("The Uchiha");
	}
	public void goku() {
		System.out.println("The Dragon Ball");
	}
}